<script>
    function initMap() {
        var uluru = {lat: 41.311081, lng: 69.240562};
        var uluru2 = {lat: 41.311081, lng: 69.240562};
        window.gglmap = new google.maps.Map(document.getElementById('map1'), {
            zoom: 15,
            mapTypeControl: false,
            center: uluru
        });

        new google.maps.Marker({
            position: uluru,
            map: gglmap
        });
        new google.maps.Marker({
            position: uluru2,
            map: gglmap
        });
    }

    function mapPanTo(lati, long) {
        var latLng = new google.maps.LatLng(lati, long); //Makes a latlng
        gglmap.panTo(latLng);
    }
</script>


