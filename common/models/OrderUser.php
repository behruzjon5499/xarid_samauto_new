 */
class OrderUser extends \yii\db\ActiveRecord
{
    public $file1;
    public $file2;
    const STATUS_WAIT = 0;
    const STATUS_ACTIVE = 10;
    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return 'order_user';
    }
